    private void btnSeciliRandevuyuSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeciliRandevuyuSilActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("RandevuVeriTabanliPU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("DELETE FROM Bulusma_1 b WHERE b.tarih=:TDID");
        if (tblDoktorBulusmalar.isRowSelected(tblDoktorBulusmalar.getSelectedRow())) {
            q.setParameter("TDID", dtm.getValueAt(tblDoktorBulusmalar.getSelectedRow(), 0));

            em.getTransaction().begin();
            q.executeUpdate();
            em.getTransaction().commit();

            em.close();
            emf.close();
            dtm.removeRow(tblDoktorBulusmalar.getSelectedRow());
        }


    }//GEN-LAST:event_btnSeciliRandevuyuSilActionPerformed

