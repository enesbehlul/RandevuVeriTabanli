    public DoktorBulusmaEkrani(Doktor_1 girisYapanDoktor) {
        initComponents();
        doktorId = girisYapanDoktor.getDoktorId();
        this.setTitle(girisYapanDoktor.getDoktorAdi() + " " + girisYapanDoktor.getDoktorSoyadi());
        dtm.addColumn("Bulusma Tarihlerim");
        dtm.addColumn("Gelecek Hasta");
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("RandevuVeriTabanliPU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT d FROM Bulusma_1 d WHERE d.doktorId=:DID");
        q.setParameter("DID", girisYapanDoktor.getDoktorId());
        List<Bulusma_1> bulusmalarim = q.getResultList();
        for (Bulusma_1 bulusma : bulusmalarim) {
            dtm.addRow(new Object[]{bulusma.getTarih(), bulusma.getKisiAdi()});
        }
        tblDoktorBulusmalar.setModel(dtm);
    }

